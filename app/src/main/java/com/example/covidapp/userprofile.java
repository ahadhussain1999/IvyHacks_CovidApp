package com.example.covidapp;

public class userprofile {
    public String user_id;
    public String name;
    public String email;
    public String number;

    public userprofile(String user_id, String name, String email, String number) {
        this.user_id = user_id;
        this.name = name;
        this.email = email;
        this.number = number;
    }
}
